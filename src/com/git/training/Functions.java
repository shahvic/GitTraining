package com.git.training;

public class Functions {

	public int add(int a, int b){
		
		return a+b;
	}
	public int subtract(int a, int b){
		return a-b;
	}
	public int square(int a){
		return a*a;
	}
	public int multiply(int a, int b){
		
		return a*b;
	}
	public double raiseTo(double a, double b){
		return Math.pow(a, b);
	}
}
